/* USER CODE BEGIN 4 */
void aumentarPWM(volatile uint32_t* canal) {
  uint32_t valor = *canal;
  while (valor < PWM_MAX) {
    valor = (PWM_MAX - valor > VALCAMBIO) ? valor + VALCAMBIO : PWM_MAX;
    *canal = valor;
    HAL_Delay(TDELAY);
  }
}

void disminuirPWM(volatile uint32_t* canal) {
  uint32_t valor = *canal;
  while (valor > 0) {
    valor = (valor > VALCAMBIO) ? valor - VALCAMBIO : 0;
    *canal = valor;
    HAL_Delay(TDELAY);
  }
}

void cicloDesafio(void) {
  // 2. Aumenta el rojo
  aumentarPWM(&TIM4->CCR2);
  // 3. Disminuye el amarillo
  disminuirPWM(&TIM4->CCR4);
  // 4. Aumenta el verde
  aumentarPWM(&TIM4->CCR1);
  // 5. Disminuye el rojo
  disminuirPWM(&TIM4->CCR2);
  // 6. Aumenta el amarillo
  aumentarPWM(&TIM4->CCR4);
  // 7. Disminuye el verde
  disminuirPWM(&TIM4->CCR1);
}

